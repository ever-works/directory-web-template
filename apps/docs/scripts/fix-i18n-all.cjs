const CATEGORIES = [
  'Getting Started', 'Architecture', 'Authentication', 'Database',
  'Type Definitions', 'API Routes', 'Services', 'Hooks', 'Components',
  'Features', 'Payment Integration', 'Internationalization', 'Configuration',
  'Content Management', 'Integrations', 'Guides', 'Development', 'Deployment',
  'Team Training', 'Advanced Guide', 'About',
];

function buildCurrentJson(locale) {
  const t = sidebarTranslations[locale];
  const obj = {
    'version.label': {
      message: t['version.label'],
      description: 'The label for version current',
    },
  };
  for (const cat of CATEGORIES) {
    obj[`sidebar.templateSidebar.category.${cat}`] = {
      message: t[cat],
      description: `The label for category '${cat}' in sidebar 'templateSidebar'`,
    };
  }
  return obj;
}

function buildNavbarJson(locale) {
  const t = navbarTranslations[locale];
  return {
    'logo.alt': { message: t.logoAlt, description: 'The alt text of navbar logo' },
    'item.label.Home': { message: t.Home, description: 'Navbar item with label Home' },
    'item.label.Help': { message: t.Help, description: 'Navbar item with label Help' },
    'item.label.GitHub': { message: t.GitHub, description: 'Navbar item with label GitHub' },
  };
}

function buildFooterJson(locale) {
  const t = footerTranslations[locale];
  const obj = {};
  for (const [key, value] of Object.entries(t)) {
    if (key === 'copyright') {
      obj[key] = { message: value, description: 'The footer copyright' };
    } else if (key.startsWith('link.title.')) {
      const title = key.replace('link.title.', '');
      obj[key] = { message: value, description: `The title of the footer links column with title=${title} in the footer` };
    } else if (key.startsWith('link.item.label.')) {
      const label = key.replace('link.item.label.', '');
      obj[key] = { message: value, description: `The label of footer link with label=${label}` };
    }
  }
  return obj;
}

function writeJsonFile(filePath, data) {
  const dir = path.dirname(filePath);
  fs.mkdirSync(dir, { recursive: true });
  fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');
  console.log(`  ✓ ${path.relative(I18N_DIR, filePath)}`);
}

// Main
const LOCALES = ['fr', 'ar', 'bg', 'zh', 'nl', 'de', 'he', 'it', 'pl', 'pt', 'ru', 'es'];
let filesWritten = 0;

console.log('=== Fixing i18n translation files ===\n');

for (const locale of LOCALES) {
  console.log(`\n[${locale}]`);

  // 1. current.json (sidebar)
  const currentPath = path.join(I18N_DIR, locale, 'docusaurus-plugin-content-docs-template', 'current.json');
  writeJsonFile(currentPath, buildCurrentJson(locale));
  filesWritten++;

  // 2. navbar.json
  const navbarPath = path.join(I18N_DIR, locale, 'docusaurus-theme-classic', 'navbar.json');
  writeJsonFile(navbarPath, buildNavbarJson(locale));
  filesWritten++;

  // 3. footer.json
  const footerPath = path.join(I18N_DIR, locale, 'docusaurus-theme-classic', 'footer.json');
  writeJsonFile(footerPath, buildFooterJson(locale));
  filesWritten++;

  // 4. code.json
  const codePath = path.join(I18N_DIR, locale, 'code.json');
  writeJsonFile(codePath, codeTranslations[locale]);
  filesWritten++;
}

console.log(`\n=== Done! ${filesWritten} files written across ${LOCALES.length} locales ===`);
