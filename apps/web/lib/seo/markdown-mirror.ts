/** Render a comparison page (two items side-by-side) to Markdown. */
export function renderComparisonMarkdown(
	comparison: Comparison,
	options: { baseUrl: string; locale?: string }
): string {
	const base = trimBase(options.baseUrl);
	const localePrefix = options.locale && options.locale !== 'en' ? `/${options.locale}` : '';
	const canonicalUrl = `${base}${localePrefix}/comparisons/${comparison.slug}`;

	const lines: string[] = [];
	lines.push(`# ${comparison.title}`);
	lines.push('');
	if (comparison.summary) {
		lines.push(`> ${comparison.summary.replace(/\n+/g, ' ')}`);
		lines.push('');
	}

	lines.push('## Compared');
	lines.push('');
	lines.push(`- **${comparison.item_a_name}** — ${base}${localePrefix}/items/${comparison.item_a_slug}`);
	lines.push(`- **${comparison.item_b_name}** — ${base}${localePrefix}/items/${comparison.item_b_slug}`);
	lines.push('');

	if (Array.isArray(comparison.dimensions) && comparison.dimensions.length > 0) {
		lines.push('## Dimensions');
		lines.push('');
		for (const dim of comparison.dimensions) {
			const name = (dim as { name?: string; title?: string }).name ?? (dim as { title?: string }).title ?? '';
			if (name) lines.push(`- ${name}`);
		}
		lines.push('');
	}

	lines.push('---');
	lines.push('');
	lines.push(`_Canonical page: ${canonicalUrl}_`);
	lines.push('');
	return lines.join('\n');
}

/** Render a static page (about/help/etc) to Markdown from its raw content. */
export function renderStaticPageMarkdown(
	pageData: { metadata?: Record<string, unknown>; content?: string } | null,
	options: { title: string; path: string; baseUrl: string; locale?: string; defaultContent?: string }
): string {
	const base = trimBase(options.baseUrl);
	const localePrefix = options.locale && options.locale !== 'en' ? `/${options.locale}` : '';
	const canonicalUrl = `${base}${localePrefix}${options.path}`;

	const title = (pageData?.metadata?.title as string) || options.title;
	const description = (pageData?.metadata?.description as string) || '';
	const lastUpdated = (pageData?.metadata?.lastUpdated as string) || '';

	const body = (pageData?.content ?? options.defaultContent ?? '').trim();

	const lines: string[] = [];
	lines.push(`# ${title}`);
	lines.push('');
	if (description) {
		lines.push(`> ${description.replace(/\n+/g, ' ')}`);
		lines.push('');
	}
	if (lastUpdated) {
		lines.push(`_Last updated: ${lastUpdated}_`);
		lines.push('');
	}
	if (body) {
		lines.push(body);
		lines.push('');
	}
	lines.push('---');
	lines.push('');
	lines.push(`_Canonical page: ${canonicalUrl}_`);
	lines.push('');

	return lines.join('\n');
}

